                            <li>Keep your mind calm by balancing your studying and personal time. Do activities which help you relax.</li>
                            <li>Do not cram things. Understand a concept and its applications through examples.</li>
                            <li>On test day, relax your mind. Don't study until the last minute.</li>
                        </ul>
